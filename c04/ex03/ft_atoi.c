int	ft_atoi(char *str)
{
	int	i;
	int	nb;
	int	signe;

	i = 0;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	signe = 0;
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			signe++;
		i++;
	}
	nb = 0;
	while (str[i] >= 48 && str[i] <= 57)
	{
		nb *= 10;
		nb += (str[i] - '0');
		i++;
	}
	if (signe % 2 != 0)
		nb *= -1;
	return (nb);
}
